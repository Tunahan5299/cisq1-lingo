package nl.hu.cisq1.lingo.trainer.domain;

public class Player {
    private int Id;
    private String Name;

    public Player(int id, String name) {
        Id = id;
        Name = name;
    }

    public int getId() {
        return Id;
    }

    public String getName() {
        return Name;
    }

    public void setId(int id) {
        Id = id;
    }

    public void setName(String name) {
        Name = name;
    }
}
